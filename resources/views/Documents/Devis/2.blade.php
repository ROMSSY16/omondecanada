        <p>Venir au Canada est un privilège et non un droit.</p>
        <p>Pour immigrer au Canada, vous devez satisfaire aux exigences établies par la loi canadienne ; tout document qui n'est pas en français ou en anglais doit être accompagné d'une traduction certifiée conforme. Si les documents requis ne sont pas présentés, le temps de traitement risque d’être plus long et la demande pourrait être refusée.</p>
